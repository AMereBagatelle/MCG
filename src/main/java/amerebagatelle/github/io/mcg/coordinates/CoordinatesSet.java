package amerebagatelle.github.io.mcg.coordinates;

public class CoordinatesSet {
    public String name;
    public int x;
    public int y;
    public int z;
    public String description;

    public CoordinatesSet(String name, int x, int y, int z, String description) {
        this.name = name;
        this.x = x;
        this.y = y;
        this.z = z;
        this.description = description;
    }
}
